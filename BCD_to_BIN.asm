DATA SEGMENT
    BCD_INPUT DB 17H
    BIN_VALUE DB ?
DATA ENDS

STACK_SEG SEGMENT
 
    STACK DW 100 DUP(0)
    TOP_STACK LABEL WORD
STACK_SEG ENDS

CODE SEGMENT
    ASSUME CS:CODE,DS:DATA,SS:STACK_SEG      
    
BCD_BIN PROC NEAR
    PUSHF
    PUSH BX
    PUSH CX
    
    MOV BL,AL
    AND BL,0FH
    AND AL,0F0H
    MOV CL,04
    ROR AL,CL
    MOV BH,0AH
    MUL BH
    
    ADD AL,BL
    
    POP CX
    POP BX
    POPF
    RET
BCD_BIN ENDP

START:
    MOV AX,DATA
    MOV DS,AX
    MOV AX,STACK_SEG
    MOV SS,AX
    MOV SP,OFFSET TOP_STACK
    
    MOV AL,BCD_INPUT
    CALL BCD_BIN
    MOV BIN_VALUE,AL
    NOP
    NOP
    NOP





CODE ENDS  
END START
